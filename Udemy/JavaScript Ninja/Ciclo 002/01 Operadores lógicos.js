// Combinam ou invertem valores booleanos

// AND - &&
let x = 5
let y = 10

console.log(x === 5 && y === 10) // True

/*
Para ser true os DOIS valores precisam ser true, exemplo:
Eu quero um lápis vermelho e um lápis verde.
Eu quero os dois, 1 só não adianta
*/

// OR ||

let num1 = 3
let num2 = 5

console.log(num1 === 3 || num2 === 6)

/* 
Para ser true, apenas um dos valores precisam ser true, exemplo:
Quero um lápis vermelho OU um lápis verde
*/


// NOT !

let name = "Matheus"
let surName = "França"

console.log(name != surName)
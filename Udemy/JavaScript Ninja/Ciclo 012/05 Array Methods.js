// Join - juntar

let foods = ["Rice", "Beans", "Egg"]
console.log(foods.join()) 

// The pattern is , (coma) but i can change this
console.log(foods.join(" ")) // Rice,Beans,Egg
console.log(foods.join("---")) // Rice---Beans---Egg


// Reverse

console.log(foods.reverse()) // He change the main array, he is permanent
console.log(foods) // Reverse
console.log(foods.reverse()) // Change to main again


// Sort - Alphabetical order
console.log(foods.sort())
let temperature = 40

if (temperature < 37) {
    console.log("Você está de boa")
} else if (temperature < 37.9) {
    console.log("Você está com febre")
} else if (temperature <= 39){
console.log("Febre aumentando")
} else {
    console.log("Vai para o médico")
}
/* 
Serve para você guardar coisas, são vagas / espaços que você coloca números, arrays, functions, etc

TEmos 3 palavras reservadas para criar uma variável
- Var
- Let
- Const
*/

var clima = "quente"
console.log(clima)

/* Podemos redeclarar uma variável */
var cor = "azul"
cor = "vermelho"
console.log(cor)

/* JS é uma linguagem fracamente tipada e estática */

let carro = true
console.log(typeof carro)

let casa = "Amarela"
console.log(typeof casa)

/* 
Let é mais atual que o var 
const = constante, não pode ser alterado
*/
